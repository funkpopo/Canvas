export type I18nKey = keyof typeof dictEn;

export function useI18n() {
  const { language } = useLanguage();

  const dict = useMemo(() => (language === "zh" ? dictZh : dictEn), [language]);

  const t = useCallback((key: I18nKey, params?: Record<string, string | number>) => {
    const template = (dict as Record<string, string>)[key] ?? key;
    if (!params) return template;
    return template.replace(/\{(\w+)\}/g, (_, k) => (params[k] ?? `{${k}}`).toString());
  }, [dict]);

  return { t, language };
}

export const i18n = { dictEn, dictZh };
